#!/bin/bash
##############################################
## dot sourcing
##############################################
DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" >/dev/null 2>&1 && pwd )"

. ${DIR}/config
. ${DIR}/functions
##############################################
## end dot sourcing
##############################################

##############################################
## randomize root password
##############################################
ROOTPW=$(gen_passwd)
echo "root:${ROOTPW}" | chpasswd
##############################################
## end randomize root password
##############################################